import turtle
t=turtle.Turtle()
turtle.Screen().bgcolor("black")
t.penup()
t.goto(0,300)
t.pendown()
t.pencolor("red")
t.fillcolor("yellow")
t.begin_fill()
t.right(70)
t.forward(100)
t.goto(0,300)
t.right(60)
t.forward(100)
t.setheading(-55)
t.circle(70,90)
t.end_fill()
t.circle(70,-10)
t.pencolor("red")
t.right(125)
t.forward(300)
t.right(90)
t.forward(80)
t.right(90)
t.forward(302)
t.penup()
t.goto(-32,-98)
t.pendown()
t.right(100)
t.pensize(10)
t.forward(250)
t.penup()
t.goto(-32,-98)
t.pendown()
t.pensize(5)
t.setheading(190)
t.circle(70,60)
t.color("yellow")
t.pensize(1)
t.begin_fill()
for i in range(8):
    t.forward(50)
    t.right(162)
t.end_fill()
t.penup()
t.goto(250,50)
t.pendown()
t.pencolor("yellow")
t.write("Happy", align="center", font=("Arial",70,"bold"))
t.goto(250,-60)
t.pendown()
t.pencolor("yellow")
t.write("Diwali", align="center", font=("Arial",70,"bold"))
turtle.done()
